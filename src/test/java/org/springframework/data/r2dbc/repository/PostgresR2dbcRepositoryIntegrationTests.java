		legoSet.setNameEquality("equality");
		legoSet.setManualReadOnly(123);

		repository.save(legoSet) //
				.as(StepVerifier::create) //
				.consumeNextWith(actual -> assertThat(actual.getManual()).isEqualTo(13))
				.verifyComplete();

