		legoSet.setName("FORSCHUNGSSCHIFF");
		legoSet.setManualReadOnly(123);

		repository.save(legoSet) //
				.as(StepVerifier::create) //
				.consumeNextWith(actual -> assertThat(actual.getManualReadOnly()).isEqualTo(22))
				.verifyComplete();

